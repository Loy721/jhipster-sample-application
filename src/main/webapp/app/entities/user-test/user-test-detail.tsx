import React, { useEffect } from 'react';
import { Link, useParams } from 'react-router-dom';
import { Button, Row, Col } from 'reactstrap';
import { Translate } from 'react-jhipster';
import { FontAwesomeIcon } from '@fortawesome/react-fontawesome';

import { useAppDispatch, useAppSelector } from 'app/config/store';

import { getEntity } from './user-test.reducer';

export const UserTestDetail = () => {
  const dispatch = useAppDispatch();

  const { id } = useParams<'id'>();

  useEffect(() => {
    dispatch(getEntity(id));
  }, []);

  const userTestEntity = useAppSelector(state => state.userTest.entity);
  return (
    <Row>
      <Col md="8">
        <h2 data-cy="userTestDetailsHeading">
          <Translate contentKey="jhipsterSampleApplicationApp.userTest.detail.title">UserTest</Translate>
        </h2>
        <dl className="jh-entity-details">
          <dt>
            <span id="id">
              <Translate contentKey="global.field.id">ID</Translate>
            </span>
          </dt>
          <dd>{userTestEntity.id}</dd>
          <dt>
            <span id="grade">
              <Translate contentKey="jhipsterSampleApplicationApp.userTest.grade">Grade</Translate>
            </span>
          </dt>
          <dd>{userTestEntity.grade}</dd>
        </dl>
        <Button tag={Link} to="/user-test" replace color="info" data-cy="entityDetailsBackButton">
          <FontAwesomeIcon icon="arrow-left" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.back">Back</Translate>
          </span>
        </Button>
        &nbsp;
        <Button tag={Link} to={`/user-test/${userTestEntity.id}/edit`} replace color="primary">
          <FontAwesomeIcon icon="pencil-alt" />{' '}
          <span className="d-none d-md-inline">
            <Translate contentKey="entity.action.edit">Edit</Translate>
          </span>
        </Button>
      </Col>
    </Row>
  );
};

export default UserTestDetail;
